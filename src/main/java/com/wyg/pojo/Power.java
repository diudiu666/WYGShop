package com.wyg.pojo;

/**
 * 用户权限类
 */
public class Power {
    private Integer id;         //权限编号
    private String name;        //权限名称

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
